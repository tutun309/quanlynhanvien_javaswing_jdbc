    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        KhachHangDAO khachHangDAO= new KhachHangDAO();
        String stk1=jTextField1.getText().toString();
        String stk2=jTextField2.getText().toString();
        String sotien=jTextField3.getText().toString();
        String mk1=jTextField4.getText().toString();
        String mk2=jTextField5.getText().toString();
        try {
            if(stk1.equals("")||stk2.equals("")||sotien.equals("")||mk1.equals("")||mk2.equals("")) throw new Exception();
            try {
                boolean check = khachHangDAO.checkNguoigui(stk1, mk1, mk2);
                if(check == false ) throw new Exception();
                int ck = khachHangDAO.Chuyenkhoan(stk1, stk2, sotien);
                if(ck==1)
                JOptionPane.showMessageDialog(rootPane, "Giao dịch thành công");
                else JOptionPane.showMessageDialog(rootPane, "Số tiền không đủ");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Thông tin nhập chưa đúng");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Hãy nhập đầy đủ thông tin");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

